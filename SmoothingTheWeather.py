def average(a, b, c):
  return (a + b + c) / 3.0

n = int(input())
a = [float(x) for x in input().split()]

smooth = []
for i in range(1, n-1):
  avg = average(a[i-1], a[i], a[i+1])
  smooth.append(avg)

