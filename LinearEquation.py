def solve_eq(x1, y1, x2, y2):
  a = (y1 - y2) // (x1 - x2)
  b = (x1 * y2 - x2 * y1) // (x1 - x2)
  return (a, b)

n = int(input())

for i in range(0, n):
  eq = [int(x) for x in input().split()]
  (a, b) = solve_eq(eq[0], eq[1], eq[2], eq[3])

  print("(" + str(a), str(b) + ")", "", end="")